			 ------------------------ INSERTING COLs FOR CHILD COVERAGE ------------------------

			-- Check if CIGADMIN.CC_LINK exists for the COVERAGE -> CAUSE_OF_LOSS
			SELECT COUNT(*) INTO v_EXISTS FROM CIGADMIN.CC_LINK
				WHERE COVERAGE = v_COV_IDX
				AND CAUSE_OF_LOSS = v_EARTHQUAKE
				AND AUDIT_ID = 'CMS-11819';
