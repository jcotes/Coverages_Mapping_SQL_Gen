				v_COV_IDX,
				'CIGADMIN',
				SYSDATE,
				'CMS-11860',
				SYSDATE,
				SYSDATE,
				'1-JAN-9999'
				);
				v_CCLINK_INSERTED := v_CCLINK_INSERTED + SQL%ROWCOUNT;
