				VALUES (CIGADMIN.SEQ_CC_LINK.nextval, 
				v_EARTHQUAKE,
				v_COV_IDX,
				'CIGADMIN',
				SYSDATE,
				'CMS-11860',
				SYSDATE,
				SYSDATE,
				'1-JAN-9999'
				);
				v_CCLINK_INSERTED := v_CCLINK_INSERTED + SQL%ROWCOUNT;
			END IF;
			v_COV_IDX := CIGADMIN.SEQ_COVERAGE.nextval;
		END LOOP;
		CLOSE c_QUAKE;

		DBMS_OUTPUT.PUT_LINE('ROWS ADDED (CIGADMIN.COVERAGE): ' || v_COVERAGES_INSERTED);
